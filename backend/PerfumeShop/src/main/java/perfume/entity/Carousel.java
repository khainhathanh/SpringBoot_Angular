package perfume.entity;

public class Carousel {
	private int id_carousel;
	private String img;
	private String alt;
	private int active;
	

	public int getId_carousel() {
		return id_carousel;
	}
	public void setId_carousel(int id_carousel) {
		this.id_carousel = id_carousel;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getAlt() {
		return alt;
	}
	public void setAlt(String alt) {
		this.alt = alt;
	}
	public int getActive() {
		return active;
	}
	public void setActive(int active) {
		this.active = active;
	}

	
}
