package perfume.entity;

public class ChildMenu {
	private int id_childMenu;
	private int id_menu;
	private String name;

	
	public int getId_childMenu() {
		return id_childMenu;
	}
	public void setId_childMenu(int id_childMenu) {
		this.id_childMenu = id_childMenu;
	}
	public int getId_menu() {
		return id_menu;
	}
	public void setId_menu(int id_menu) {
		this.id_menu = id_menu;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
